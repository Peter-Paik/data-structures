/*
 * Pair Class
 * pairs two numbers like coordinates
 */
public class Pair {
    int row;
    int col;
    public Pair(int row, int col){
        this.row = row;
        this.col = col;
    }
    public int getRow(){
        return this.row;
    }
    public int getColumn(){
        return this.col;
    }
}
